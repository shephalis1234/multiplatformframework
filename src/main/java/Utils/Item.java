package Utils;

public class Item {
    private String itemNo;
    private int qty;

    /**
     * @return the itemNo
     */
    public String getItemNo() {
        return itemNo;
    }
    /**
     * @param itemNo the itemNo to set
     */
    public void setItemNo(String itemNo) {
        this.itemNo = itemNo;
    }
    /**
     * @return the qty
     */
    public int getQty() {
        return qty;
    }
    /**
     * @param qty the qty to set
     */
    public void setQty(int qty) {
        this.qty = qty;
    }

}
